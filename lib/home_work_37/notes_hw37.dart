import 'package:flutter/material.dart';

class Hw37NotesPage extends StatelessWidget {
  const Hw37NotesPage({super.key});

  @override
  Widget build(BuildContext context) {
    const text = '''
HW37 — Singleton / Factory

1. Singleton
Что это:
- паттерн, который гарантирует один экземпляр объекта во всём приложении.

Где использовать:
- Logger
- AnalyticsService
- ConfigService

Плюсы:
- единая точка доступа
- не создаётся много одинаковых объектов
- удобно для логирования и глобальных сервисов

Минусы:
- скрытая глобальная зависимость
- сложнее тестировать, если злоупотреблять
- может привести к сильной связанности

2. Factory
Что это:
- паттерн для создания объектов через отдельную фабрику.

Где использовать:
- создание widget по status enum
- парсинг API-ответов по type
- создание разных стратегий / моделей

Плюсы:
- централизует создание объектов
- убирает if/switch из UI
- легче расширять

Минусы:
- ещё один уровень абстракции
- для простых случаев может быть избыточен

3. Сравнение Singleton vs DI (get_it)

Singleton — плюсы:
1) просто реализовать
2) легко получить доступ из любого места
3) удобно для logger/analytics
4) не нужно отдельно регистрировать
5) один экземпляр гарантирован

Singleton — минусы:
1) глобальное состояние
2) сильная связанность
3) сложнее мокать в тестах
4) хуже масштабируется при росте проекта
5) легко начать использовать везде без контроля

DI через get_it — плюсы:
1) явное управление зависимостями
2) легче подменять моки
3) лучше тестируемость
4) чище архитектура
5) подходит для больших проектов

DI через get_it — минусы:
1) больше настройки
2) больше шаблонного кода
3) новичкам сложнее понять
4) ошибки регистрации ловятся не сразу
5) для маленького проекта может быть избыточен
''';

    return Scaffold(
      appBar: AppBar(title: const Text('HW37 Notes')),
      body: const Padding(
        padding: EdgeInsets.all(16),
        child: SingleChildScrollView(child: Text(text)),
      ),
    );
  }
}
