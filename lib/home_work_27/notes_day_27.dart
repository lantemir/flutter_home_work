/// Day 27 — flutter_bloc (BLoC/Cubit)
///
/// 1) Зачем нужен BLoC
/// - Разделяет UI и бизнес-логику.
/// - Поток: события/действия -> новое состояние.
/// - Предсказуемость: одно действие = одно состояние, легко тестировать.
///
/// Когда применять:
/// - Сложные формы/экраны: loading/success/error.
/// - Много источников данных (API + кэш) и нужна синхронизация.
/// - Когда state должен жить дольше одного виджета.
///
/// Ошибки/подводные камни:
/// - Держать в state "BuildContext" или контроллеры TextEditingController (нельзя).
/// - Делать тяжелую логику внутри build() вместо bloc/cubit.
/// - Слишком крупный state без Equatable -> лишние rebuild.
///
/// 2) Cubit
/// - Более простой BLoC: нет событий, только методы.
/// - Ты напрямую вызываешь cubit.increment() -> emit(newState)
///
/// Когда применять:
/// - Простые экраны: счётчик, переключатели, фильтры.
/// - Когда "события" по сути = методы.
///
/// 3) BlocProvider / BlocBuilder / BlocListener
/// - BlocProvider: кладёт bloc/cubit в дерево.
/// - BlocBuilder: перерисовывает UI при изменениях state.
/// - BlocListener: выполняет побочные эффекты (SnackBar, навигация),
///   не перерисовывает UI.
///
/// 4) Паттерн состояний loading/success/error
/// - Стандарт для сетевых операций.
/// - UI читается легче: switch(state.status) ...
///
/// Самопроверка:
/// Q1: Чем BlocListener отличается от BlocBuilder?
/// A1: Listener для side-effects (навиг./снэкбар), Builder — для UI.
///
/// Q2: Почему лучше делать state immutable?
/// A2: Предсказуемость и корректное сравнение state, меньше багов.
